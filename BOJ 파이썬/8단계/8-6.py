# 분수찾기 (1193번)
# 1/1 -> 1/2 -> 2/1 -> 3/1 -> 2/2 -> 1/3 -> 1/4 -> 2/3 ......
# 분자 : 1, 1,2, 3,2,1, 1,2,3,4, 5,4,3,2,1 ..... (짝수는 오름차순, 홀수는 내림차순)
# 분모 : 1, 2,1, 1,2,3, 4,3,2,1, 1,2,3,4,5 ..... (짝수는 내림차순, 홀수는 오름차순)

X = int(input())
line = 1 # 홀수부터 시작

while X > line: # 3>1 -> 2, 2
    X -= line # 3-1 = 2
    line += 1 # 1+1 = 2

if line%2 == 0:
    a = X #2(분자)
    b = line-X+1 #분모
else:
    a = line-X+1
    b = X
print(a,'/',b, sep="")
